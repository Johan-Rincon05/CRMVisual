function showCareerDetails(careerId) {
    const modal = document.getElementById('careerModal');
    const modalContent = document.getElementById('modalContent');
    const career = careerData[careerId];

    modalContent.innerHTML = `
        <h2>${career.title}</h2>
        <div class="career-details">
            <section>
                <h3>Descripción</h3>
                <p>${career.description}</p>
            </section>
            
            <section>
                <h3>Duración</h3>
                <p>${career.duration}</p>
            </section>

            <section>
                <h3>Documentos Requeridos</h3>
                <ul>
                    ${career.documents.map(doc => `<li>${doc}</li>`).join('')}
                </ul>
            </section>

            <section>
                <h3>Costos</h3>
                <ul>
                    <li>Suscripción: $${career.costs.subscription}</li>
                    <li>Matrícula: $${career.costs.enrollment}</li>
<li>Beca: ${career.costs.scholarship}</li>
                    <li>Mensualidad: $${career.costs.monthly}</li>
                </ul>
            </section>
        </div>
    `;

    modal.style.display = 'block';
}

// Close modal when clicking outside or on close button
window.onclick = function(event) {
    const modal = document.getElementById('careerModal');
    if (event.target == modal || event.target.classList.contains('close-button')) {
        modal.style.display = 'none';
    }
}

// Add keyboard support for closing modal with ESC key
document.addEventListener('keydown', function(event) {
    const modal = document.getElementById('careerModal');
    if (event.key === 'Escape' && modal.style.display === 'block') {
        modal.style.display = 'none';
    }
});

// Add smooth transition for modal opening
document.addEventListener('DOMContentLoaded', function() {
    const modal = document.getElementById('careerModal');
    modal.style.transition = 'opacity 0.3s ease-in-out';
});


